function perimeter(x1, y1) {
    var p = (x1 * 2) + (y1 * 2);
    return p; 
}

var case1 = 10;
var case2 = 7;
var result1 = perimeter(case1, case2);
console.log(result1);

function area(x1, y1) {
    var a = (x1 * x1 * x1) + (y1 * y1 * y1);
    return a;
}

case1 = 10;
case2 = 7;
result1 = area(case1, case2);
console.log(result1);

