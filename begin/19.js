function perimeter(x1, y1) {
    var P = (x1 * 2) + (y1 * 2);
    return P;
}

var case1 = 3;
var case2 = 4;
var result1 = perimeter(case1, case2);
console.log(result1);

function area(x1, y1) {
    var A = (x1 * x1 * x1) + (y1 * y1 * y1);
    return A;
}

case1 = 3;
case2 = 4;
result1 = area(case1, case2);
console.log(result1);